<b>User Notes</b>

<p>The user notes field is used as a simple
means of communication between the administrator
and the user.</p>

<p>Beware that this text can be edited or changed
by the user, either to respond to an administrator
note, or simply to hold information useful
for the user.</p>

<p>Use the administrator notes field to record
information which the user can not see or alter.</p>
