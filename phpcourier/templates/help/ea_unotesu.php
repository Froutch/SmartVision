<b>User Notes</b>

<p>The note field is used as a simple communication between
you and the administrator. Be careful to read any note
left for you.</p>

<p>You may edit this note field, either to return a message
to the administrator, or to remember something for yourself.
</p>

<p>NOTE that adding text to this field <u>does not</u>
alert or notify the administrator. It simply allows the
administrator to see the note if the account is viewed.<p>
